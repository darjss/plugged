/**
 * Unified seed script — generates a single SQL file that idempotently
 * seeds: brands, categories, products, product_category, product_variant,
 * iem_spec, and product_image.
 *
 * Data sources:
 *   - iem-yangkeduo-prices-en.json  (products, brands, variants, prices)
 *   - seed-images.json              (R2 image URLs already uploaded)
 *
 * Usage:
 *   node scripts/build-seed-sql.mjs > seed.sql
 *   wrangler d1 execute plugged --remote --file seed.sql
 */
import { readFileSync } from "node:fs";

const repoRoot = new URL("../", import.meta.url);
const source = JSON.parse(readFileSync(new URL("iem-yangkeduo-prices-en.json", repoRoot), "utf8"));
const imageMap = JSON.parse(readFileSync(new URL("seed-images.json", repoRoot), "utf8"));

// ── Pricing ──────────────────────────────────────────────────────────
const YUAN_TO_MNT = 500;
const MARKUP_TIER1 = 1.5; // ≤ 100 CNY
const MARKUP_TIER2 = 1.75; // > 100 CNY
const TIER_THRESHOLD = 100;

function moneyMnt(yuan) {
  const p = Number.parseFloat(String(yuan ?? "").replace(/[^\d.]/g, ""));
  if (!Number.isFinite(p)) return 0;
  const markup = p > TIER_THRESHOLD ? MARKUP_TIER2 : MARKUP_TIER1;
  return Math.ceil((p * YUAN_TO_MNT * markup) / 5000) * 5000;
}

// ── Helpers ──────────────────────────────────────────────────────────
function sql(value) {
  if (value === null || value === undefined || value === "") return "NULL";
  if (typeof value === "number") return String(value);
  return `'${String(value).replaceAll("'", "''")}'`;
}

function slugify(value) {
  return String(value)
    .toLowerCase()
    .replaceAll(/[^a-z0-9]+/g, "-")
    .replaceAll(/^-|-$/g, "")
    .slice(0, 96);
}

function titleCase(value) {
  return String(value)
    .split(/\s+/)
    .filter(Boolean)
    .map((p) => (p.length <= 3 ? p.toUpperCase() : p[0].toUpperCase() + p.slice(1)))
    .join(" ");
}

// ── Category definitions ─────────────────────────────────────────────
const categories = [
  {
    id: "category_iem",
    slug: "iem",
    name: "IEMs",
    description: "In-ear monitors — wired earphones tuned for hi-fi listening.",
  },
  {
    id: "category_dac",
    slug: "dac",
    name: "DAC Amps",
    description: "Portable DAC amplifiers — dongles that drive your IEMs.",
  },
];

const categoryByJsonCategory = {
  IEM: "category_iem",
  DAC: "category_dac",
};

// ── Brand detection ──────────────────────────────────────────────────
const knownBrands = [
  "7hz",
  "cca",
  "gk",
  "kiwi ears",
  "kz",
  "moondrop",
  "qkz",
  "simgot",
  "tangzu",
  "trn",
  "truthear",
  "dunu",
  "kefine",
  "jcally",
  "cally",
];

function brandNameFor(result) {
  const haystack = `${result.query} ${result.matched_search_result?.title ?? ""}`.toLowerCase();
  const brand = knownBrands.find((c) => haystack.includes(c));
  return brand ? titleCase(brand) : titleCase(result.query.split(/\s+/)[0] ?? "IEM");
}

function productNameFor(result) {
  return titleCase(result.query.replaceAll(/\s+/g, " ").trim());
}

// ── Generate SQL ─────────────────────────────────────────────────────
const okResults = source.results.filter((r) => r.status === "ok");
const statements = [
  "-- Unified seed: brands, categories, products, product_category,",
  "-- product_variant, iem_spec, product_image.",
  "-- Generated by scripts/build-seed-sql.mjs — idempotent (safe to re-run).",
  "",
  "-- ── Categories ──────────────────────────────────────────────────",
];

// Categories
for (const cat of categories) {
  statements.push(
    `INSERT INTO category (id, slug, name, description, created_at, updated_at) VALUES (${sql(cat.id)}, ${sql(cat.slug)}, ${sql(cat.name)}, ${sql(cat.description)}, unixepoch(), unixepoch()) ON CONFLICT(id) DO UPDATE SET slug = excluded.slug, name = excluded.name, description = excluded.description, updated_at = unixepoch();`,
  );
}

statements.push("", "-- ── Brands ──────────────────────────────────────────────────────");

const brandByName = new Map();
for (const result of okResults) {
  const brandName = brandNameFor(result);
  const brandSlug = slugify(brandName);
  const brandId = `brand_${brandSlug}`;
  if (!brandByName.has(brandName)) {
    brandByName.set(brandName, brandId);
    statements.push(
      `INSERT INTO brand (id, slug, name, description, website_url, created_at, updated_at) VALUES (${sql(brandId)}, ${sql(brandSlug)}, ${sql(brandName)}, NULL, NULL, unixepoch(), unixepoch()) ON CONFLICT(id) DO UPDATE SET slug = excluded.slug, name = excluded.name, updated_at = unixepoch();`,
    );
  }
}

statements.push("", "-- ── Products + categories + variants + specs + images ───────────");

for (const result of okResults) {
  const brandName = brandNameFor(result);
  const brandId = brandByName.get(brandName);
  const productName = productNameFor(result);
  const productSlug = slugify(productName);
  const productId = `product_${productSlug}`;
  const basePriceMnt = moneyMnt(result.base_price_yuan);
  const description = [
    result.matched_search_result?.title,
    result.product_title,
    result.product_url ? `Source: ${result.product_url}` : null,
  ]
    .filter(Boolean)
    .join("\n\n");

  // Product
  statements.push(
    `INSERT INTO product (id, slug, brand_id, name, short_description, description, status, base_price_mnt, compare_at_price_mnt, currency, featured, created_at, updated_at) VALUES (${sql(productId)}, ${sql(productSlug)}, ${sql(brandId)}, ${sql(productName)}, ${sql(result.matched_search_result?.title ?? null)}, ${sql(description)}, 'active', ${basePriceMnt}, NULL, 'MNT', 0, unixepoch(), unixepoch()) ON CONFLICT(id) DO UPDATE SET slug = excluded.slug, brand_id = excluded.brand_id, name = excluded.name, short_description = excluded.short_description, description = excluded.description, status = excluded.status, base_price_mnt = excluded.base_price_mnt, updated_at = unixepoch();`,
  );

  // Product → Category
  const categoryId = categoryByJsonCategory[result.category] ?? "category_iem";
  statements.push(
    `INSERT INTO product_category (product_id, category_id) VALUES (${sql(productId)}, ${sql(categoryId)}) ON CONFLICT(product_id, category_id) DO NOTHING;`,
  );

  // IEM spec (stub — filled in manually or via admin)
  statements.push(
    `INSERT INTO iem_spec (product_id, driver_type, driver_config, impedance_ohms, sensitivity_db, frequency_response, connector, cable, mic, shell_material, nozzle_material, sound_signature, fit, included_accessories) VALUES (${sql(productId)}, NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL, NULL) ON CONFLICT(product_id) DO NOTHING;`,
  );

  // Variants
  const variations = result.variations?.length
    ? result.variations
    : [{ selected: productName, price_yuan: result.base_price_yuan }];

  for (const [index, variation] of variations.entries()) {
    const variantName = variation.selected || productName;
    const variantSlug = slugify(`${productSlug}-${variantName || index}`);
    const variantId = `variant_${variantSlug}`.slice(0, 120);
    const sku = `PLG-${variantSlug}`.toUpperCase().slice(0, 80);
    const priceMnt = moneyMnt(variation.price_yuan || result.base_price_yuan);
    const compareAtPriceMnt = moneyMnt(variation.promo_price_before_coupon_yuan);

    statements.push(
      `INSERT INTO product_variant (id, product_id, sku, name, price_mnt, compare_at_price_mnt, stock_quantity, reserved_quantity, active, created_at, updated_at) VALUES (${sql(variantId)}, ${sql(productId)}, ${sql(sku)}, ${sql(variantName)}, ${priceMnt}, ${compareAtPriceMnt || "NULL"}, 10, 0, 1, unixepoch(), unixepoch()) ON CONFLICT(id) DO UPDATE SET sku = excluded.sku, name = excluded.name, price_mnt = excluded.price_mnt, compare_at_price_mnt = excluded.compare_at_price_mnt, active = excluded.active, updated_at = unixepoch();`,
    );
  }

  // Images (from seed-images.json — already uploaded to R2)
  const images = imageMap[productId] ?? [];
  for (const img of images) {
    statements.push(
      `INSERT INTO product_image (id, product_id, r2_key, url, alt, sort_order, is_primary, created_at) VALUES (${sql(img.id)}, ${sql(img.product_id)}, ${sql(img.r2_key)}, ${sql(img.url)}, ${sql(img.alt)}, ${img.sort_order}, ${img.is_primary}, unixepoch()) ON CONFLICT(id) DO UPDATE SET url = excluded.url, r2_key = excluded.r2_key, alt = excluded.alt, sort_order = excluded.sort_order, is_primary = excluded.is_primary;`,
    );
  }
}

console.log(statements.join("\n"));
