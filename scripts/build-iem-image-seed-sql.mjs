import { randomUUID } from "node:crypto";
import { mkdir, rm, writeFile } from "node:fs/promises";
import { join, resolve } from "node:path";
import { readFileSync } from "node:fs";
import Firecrawl from "@mendable/firecrawl-js";

const repoRoot = new URL("../", import.meta.url);
const sourcePath = new URL("iem-yangkeduo-prices-en.json", repoRoot);
const source = JSON.parse(readFileSync(sourcePath, "utf8"));

const vitStoreEnvPath = "/home/darjs/dev/vit-store/.env";
const apiKey = process.env.FIRECRAWL_API_KEY || readEnvValue(vitStoreEnvPath, "FIRECRAWL_API_KEY");
const limit = Number.parseInt(
  process.argv.find((arg) => arg.startsWith("--limit="))?.split("=")[1] ?? "20",
  10,
);
const BUCKET = "plugged";
const TMP_DIR = resolve("tmp/plugged-product-images");

if (!apiKey) {
  throw new Error("FIRECRAWL_API_KEY is missing. Set it or keep vit-store .env available.");
}

const firecrawl = new Firecrawl({ apiKey });

const amazonProductSchema = {
  type: "object",
  properties: {
    title: { type: "string" },
    brand: { type: "string" },
    images: {
      type: "array",
      items: { type: "string" },
    },
  },
  required: ["title"],
};

const rows = source.results.filter((result) => result.status === "ok").slice(0, limit);

await mkdir(TMP_DIR, { recursive: true });

const statements = [
  "-- Generated by scripts/build-iem-image-seed-sql.mjs from Amazon product page scrapes.",
  "-- Images are downloaded from Amazon, uploaded to R2, and served via /img/ worker route.",
  "-- Re-run safely: image ids are stable and upserted.",
];

for (const [productIndex, result] of rows.entries()) {
  const productName = productNameFor(result);
  const productSlug = slugify(productName);
  const productId = `product_${productSlug}`;

  const scraped = await findAndScrapeAmazonProduct(productName);
  if (!scraped) {
    console.error(`SKIP: ${productName} — no Amazon product with usable images`);
    continue;
  }

  const score = tokenMatchScore(productName, scraped.title);
  if (score < 0.3) {
    console.error(`SKIP: ${productName} — low match (${score.toFixed(2)}) with "${scraped.title}"`);
    continue;
  }

  const imageUrls = scraped.images.slice(0, 4);
  if (imageUrls.length === 0) {
    console.error(`SKIP: ${productName} — no image URLs after filtering`);
    continue;
  }

  const uploaded = await uploadImagesToR2(imageUrls, productId);
  if (uploaded.length === 0) {
    console.error(`SKIP: ${productName} — all R2 uploads failed`);
    continue;
  }

  console.error(
    `OK: ${productName} -> ${uploaded.length} images (match ${score.toFixed(2)} with "${scraped.title}")`,
  );

  for (const [imageIndex, { r2Key, url }] of uploaded.entries()) {
    const id = `image_${productSlug}_${imageIndex + 1}`;
    statements.push(
      `INSERT INTO product_image (id, product_id, r2_key, url, alt, sort_order, is_primary, created_at) VALUES (${sql(id)}, ${sql(productId)}, ${sql(r2Key)}, ${sql(url)}, ${sql(`${productName} product image`)}, ${imageIndex}, ${imageIndex === 0 ? 1 : 0}, unixepoch()) ON CONFLICT(id) DO UPDATE SET r2_key = excluded.r2_key, url = excluded.url, alt = excluded.alt, sort_order = excluded.sort_order, is_primary = excluded.is_primary;`,
    );
  }

  if (productIndex < rows.length - 1) {
    await sleep(300);
  }
}

console.log(statements.join("\n"));
await rm(TMP_DIR, { recursive: true, force: true });

// --- Amazon search + scrape (adapted from vit-store backfill script) ---

async function findAndScrapeAmazonProduct(query) {
  const searchResponse = await firecrawl.search(`site:amazon.com ${query} IEM`, {
    limit: 5,
  });
  const urls =
    searchResponse.web
      ?.map((result) => ("url" in result ? result.url : null))
      .filter((url) => url && url.includes("amazon.com"))
      .filter((url) => url.includes("/dp/") || url.includes("/gp/product/")) ?? [];

  for (const url of urls.slice(0, 3)) {
    const scraped = await scrapeAmazonProduct(url);
    if (scraped?.images.length) return scraped;
  }

  return null;
}

async function scrapeAmazonProduct(url) {
  try {
    const response = await firecrawl.scrape(url, {
      formats: [{ type: "json", schema: amazonProductSchema }, "html"],
    });
    const jsonData = response.json || {};
    const html = response.html || "";
    const title = String(jsonData.title || "").trim();
    const images = [
      ...extractAmazonImageIds(html).map(toHighResUrl),
      ...(Array.isArray(jsonData.images) ? jsonData.images : []),
    ].filter((image) => typeof image === "string");

    if (!title || images.length === 0) return null;
    return { title, images: unique(images) };
  } catch (error) {
    console.error(
      `Scrape failed: ${url} ${error instanceof Error ? error.message : String(error)}`,
    );
    return null;
  }
}

function extractAmazonImageIds(html) {
  const imageIds = new Set();
  const colorImagesMatch = html.match(
    /'colorImages'\s*:\s*\{\s*'initial'\s*:\s*(\[[\s\S]*?\])\s*\}/,
  );
  if (colorImagesMatch) {
    try {
      const imagesData = JSON.parse(colorImagesMatch[1] ?? "[]");
      for (const image of imagesData) {
        const url = image.hiRes || image.large || Object.values(image.main || {})[0];
        const id = amazonImageIdFromUrl(url);
        if (id) imageIds.add(id);
      }
    } catch {}
  }

  for (const match of html.matchAll(/data-old-hires="([^"]+)"/g)) {
    const id = amazonImageIdFromUrl(match[1]);
    if (id) imageIds.add(id);
  }
  for (const match of html.matchAll(/\/images\/I\/([A-Za-z0-9\-_+%]+)\._[^"]+"/g)) {
    imageIds.add(match[1]);
  }

  return Array.from(imageIds).slice(0, 10);
}

function amazonImageIdFromUrl(url) {
  const match = url?.match(/\/images\/I\/([A-Za-z0-9\-_+%]+)\./);
  return match?.[1] ?? null;
}

function toHighResUrl(imageId) {
  return `https://m.media-amazon.com/images/I/${imageId}._AC_SL1500_.jpg`;
}

// --- R2 upload (adapted from vit-store backfill script) ---

async function uploadImagesToR2(imageUrls, productId) {
  const uploaded = [];
  for (const sourceUrl of imageUrls) {
    const img = await downloadImage(sourceUrl);
    if (!img) continue;

    const ext = extFromContentType(img.contentType);
    const id = randomUUID().replace(/-/g, "");
    const r2Key = `products/${productId}/${id}.${ext}`;
    const localPath = join(TMP_DIR, `${id}.${ext}`);
    await writeFile(localPath, img.bytes);

    const result = spawnSync(
      "wrangler",
      [
        "r2",
        "object",
        "put",
        `${BUCKET}/${r2Key}`,
        "--file",
        localPath,
        "--content-type",
        img.contentType,
        "--remote",
      ],
      { encoding: "utf8" },
    );

    if (result.status === 0) {
      uploaded.push({ r2Key, url: `/img/${r2Key}` });
    } else {
      console.error(`R2 upload failed: ${r2Key} — ${result.stderr?.slice(0, 200)}`);
    }
  }
  return uploaded;
}

async function downloadImage(url) {
  try {
    const response = await fetch(url, {
      headers: {
        "User-Agent": "Mozilla/5.0 (Windows NT 10.0; Win64; x64) AppleWebKit/537.36",
        Accept: "image/*",
      },
      signal: AbortSignal.timeout(15_000),
    });
    if (!response.ok) return null;
    const contentType = response.headers.get("content-type") || "";
    if (!contentType.startsWith("image/")) return null;
    return {
      bytes: new Uint8Array(await response.arrayBuffer()),
      contentType,
    };
  } catch {
    return null;
  }
}

// --- Helpers ---

function tokenMatchScore(expected, actual) {
  const expectedTokens = new Set(normalize(expected).split(" ").filter(Boolean));
  const actualTokens = normalize(actual).split(" ").filter(Boolean);
  if (expectedTokens.size === 0) return 0;
  let hits = 0;
  for (const token of actualTokens) {
    if (expectedTokens.has(token)) hits++;
  }
  return hits / expectedTokens.size;
}

function normalize(value) {
  return value
    .toLowerCase()
    .normalize("NFKD")
    .replace(/[^a-z0-9]+/g, " ")
    .replace(/\b(the|and|for|with|edition|generation|ii|2nd|3rd)\b/g, " ")
    .replace(/\s+/g, " ")
    .trim();
}

function extFromContentType(contentType) {
  if (contentType.includes("png")) return "png";
  if (contentType.includes("gif")) return "gif";
  if (contentType.includes("webp")) return "webp";
  return "jpg";
}

function readEnvValue(path, key) {
  try {
    const env = readFileSync(path, "utf8");
    const line = env.split(/\r?\n/).find((candidate) => candidate.trim().startsWith(`${key}=`));
    return line?.slice(key.length + 1).trim() || "";
  } catch {
    return "";
  }
}

function unique(values) {
  return Array.from(new Set(values));
}

function sql(value) {
  if (value === null || value === undefined || value === "") return "NULL";
  if (typeof value === "number") return String(value);
  return `'${String(value).replaceAll("'", "''")}'`;
}

function slugify(value) {
  return String(value)
    .toLowerCase()
    .replaceAll(/[^a-z0-9]+/g, "-")
    .replaceAll(/^-|-$/g, "")
    .slice(0, 96);
}

function titleCase(value) {
  return String(value)
    .split(/\s+/)
    .filter(Boolean)
    .map((part) => (part.length <= 3 ? part.toUpperCase() : part[0].toUpperCase() + part.slice(1)))
    .join(" ");
}

function productNameFor(result) {
  return titleCase(result.query.replaceAll(/\s+/g, " ").trim());
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}
