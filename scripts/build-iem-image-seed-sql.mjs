import { readFileSync } from "node:fs";
import Firecrawl from "@mendable/firecrawl-js";

const repoRoot = new URL("../", import.meta.url);
const sourcePath = new URL("scripts/data/iem-yangkeduo-prices-en.json", repoRoot);
const source = JSON.parse(readFileSync(sourcePath, "utf8"));

const projectEnvPath = new URL(".env", repoRoot).pathname;
const apiKey = process.env.FIRECRAWL_API_KEY || readEnvValue(projectEnvPath, "FIRECRAWL_API_KEY");
const limit = Number.parseInt(
  process.argv.find((arg) => arg.startsWith("--limit="))?.split("=")[1] ?? "20",
  10,
);

if (!apiKey) {
  throw new Error(
    `FIRECRAWL_API_KEY not found. Set it in the environment or in ${projectEnvPath}.`,
  );
}

const firecrawl = new Firecrawl({ apiKey });
const rows = source.results
  .filter((result) => result.status === "ok" && result.product_url)
  .slice(0, limit);
const statements = [
  "-- Generated by scripts/build-iem-image-seed-sql.mjs from Firecrawl scrapes.",
  "-- Re-run safely: image ids are stable and upserted.",
];

for (const [productIndex, result] of rows.entries()) {
  const productSlug = slugify(productNameFor(result));
  const productId = `product_${productSlug}`;
  const urls = await resolveImageUrls(result);

  if (urls.length === 0) {
    console.error(`No images: ${result.query}`);
    continue;
  }

  console.error(`Images: ${result.query} -> ${urls.length}`);

  for (const [imageIndex, url] of urls.slice(0, 6).entries()) {
    const id = `image_${productSlug}_${imageIndex + 1}`;
    statements.push(
      `INSERT INTO product_image (id, product_id, r2_key, url, alt, sort_order, is_primary, created_at) VALUES (${sql(id)}, ${sql(productId)}, NULL, ${sql(url)}, ${sql(`${productNameFor(result)} product image`)}, ${imageIndex}, ${imageIndex === 0 ? 1 : 0}, unixepoch()) ON CONFLICT(id) DO UPDATE SET url = excluded.url, alt = excluded.alt, sort_order = excluded.sort_order, is_primary = excluded.is_primary;`,
    );
  }

  if (productIndex < rows.length - 1) {
    await sleep(250);
  }
}

console.log(statements.join("\n"));

function readEnvValue(path, key) {
  try {
    const env = readFileSync(path, "utf8");
    const line = env.split(/\r?\n/).find((candidate) => candidate.trim().startsWith(`${key}=`));
    return line?.slice(key.length + 1).trim() || "";
  } catch {
    return "";
  }
}

async function resolveImageUrls(result) {
  const directUrls = await scrapeImageUrls(result.product_url);

  if (directUrls.length > 0) return directUrls;

  const candidates = await searchProductPages(result.query);

  for (const candidate of candidates) {
    const urls = await scrapeImageUrls(candidate);

    if (urls.length > 0) return urls;
  }

  return [];
}

async function searchProductPages(query) {
  try {
    const response = await firecrawl.search(`${query} IEM product images`, {
      limit: 5,
    });
    return (response.web ?? [])
      .map((result) => ("url" in result ? result.url : ""))
      .filter(Boolean)
      .filter((url) => !url.includes("youtube.com"))
      .filter((url) => !url.includes("reddit.com"))
      .filter((url) => !url.includes("facebook.com"))
      .filter((url) => !url.includes("x.com"));
  } catch (error) {
    console.error(
      `Search failed: ${query} ${error instanceof Error ? error.message : String(error)}`,
    );
    return [];
  }
}

async function scrapeImageUrls(url) {
  try {
    const response = await firecrawl.scrape(url, {
      formats: ["html", "markdown"],
      onlyMainContent: false,
    });
    const text = `${response.html ?? ""}\n${response.markdown ?? ""}`;
    return unique(extractUrls(text).filter(isLikelyProductImage));
  } catch (error) {
    console.error(
      `Scrape failed: ${url} ${error instanceof Error ? error.message : String(error)}`,
    );
    return [];
  }
}

function extractUrls(text) {
  const urls = [];
  const patterns = [/https?:\/\/[^"'`\s<>\\)]+/g, /\/\/[^"'`\s<>\\)]+/g];

  for (const pattern of patterns) {
    for (const match of text.matchAll(pattern)) {
      const raw = match[0].replaceAll("&amp;", "&");
      urls.push(raw.startsWith("//") ? `https:${raw}` : raw);
    }
  }

  return urls;
}

function isLikelyProductImage(url) {
  const lower = url.toLowerCase();
  if (!/\.(avif|gif|jpe?g|png|webp)(\?|$)/.test(lower)) return false;
  if (lower.includes("m.media-amazon.com/images/g/")) return false;
  if (lower.includes("_ac_ss")) return false;
  if (lower.includes("_ss")) return false;
  if (lower.includes("avatar")) return false;
  if (lower.includes("icon")) return false;
  if (lower.includes("logo")) return false;
  if (lower.includes("nav")) return false;
  if (lower.includes("sprite")) return false;
  return true;
}

function unique(values) {
  return Array.from(new Set(values));
}

function sql(value) {
  if (value === null || value === undefined || value === "") return "NULL";
  if (typeof value === "number") return String(value);
  return `'${String(value).replaceAll("'", "''")}'`;
}

function slugify(value) {
  return String(value)
    .toLowerCase()
    .replaceAll(/[^a-z0-9]+/g, "-")
    .replaceAll(/^-|-$/g, "")
    .slice(0, 96);
}

function titleCase(value) {
  return String(value)
    .split(/\s+/)
    .filter(Boolean)
    .map((part) => (part.length <= 3 ? part.toUpperCase() : part[0].toUpperCase() + part.slice(1)))
    .join(" ");
}

function productNameFor(result) {
  return titleCase(result.query.replaceAll(/\s+/g, " ").trim());
}

function sleep(ms) {
  return new Promise((resolve) => setTimeout(resolve, ms));
}
